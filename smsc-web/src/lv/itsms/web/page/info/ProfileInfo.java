package lv.itsms.web.page.info;

public class ProfileInfo {

	String txtHeader;
	String txtName;
	String txtSurname;
	String txtLogin;
	String txtEmail;
	String lng;
	String txtError;
	String txtErrorReturn;
	public String getTxtHeader() {
		return txtHeader;
	}
	public void setTxtHeader(String txtHeader) {
		this.txtHeader = txtHeader;
	}
	public String getTxtName() {
		return txtName;
	}
	public void setTxtName(String txtName) {
		this.txtName = txtName;
	}
	public String getTxtSurname() {
		return txtSurname;
	}
	public void setTxtSurname(String txtSurname) {
		this.txtSurname = txtSurname;
	}
	public String getTxtLogin() {
		return txtLogin;
	}
	public void setTxtLogin(String txtLogin) {
		this.txtLogin = txtLogin;
	}
	public String getTxtEmail() {
		return txtEmail;
	}
	public void setTxtEmail(String txtEmail) {
		this.txtEmail = txtEmail;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getTxtError() {
		return txtError;
	}
	public void setTxtError(String txtError) {
		this.txtError = txtError;
	}
	public String getTxtErrorReturn() {
		return txtErrorReturn;
	}
	public void setTxtErrorReturn(String txtErrorReturn) {
		this.txtErrorReturn = txtErrorReturn;
	}
}
